public class TypyProste {
    public static void main(String[] args) {

        //Liczby Stałoprzecinkowe
        byte firstNumber = 127; // 1 bajt -128 do 127
        short secondNumber = 32689; // 2 bajty -32768 do 32767
        int thirdNumber = 327668999; // 4 bajty -2 147 483 648 do 2 147 483 647
        long fourthNumber = 2148000L; // 8 bajtów -2^63 do (2^63) -1

        // liczby zmienno przecinkowe
        float fiftNumber = 4.99934F;
        double sixthNumber = 3.999999999999999D;

        //Wartość logiczna
        boolean prawda = true;
        boolean falsz = false;

        //Pojedynczy znak
        char letter = 'a';
    }
}
