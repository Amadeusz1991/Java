public class PętlaFor {
    public static void main(String[] args) {

        int number = 100;

        for (int i = 0; i < number; i = i + 2){
            System.out.print(i);
        }
        for (int i = 0; i < 100; i++){
            if(i % 5 == 0);
            System.out.println(i);
        }
        for (int sth = 0; sth < 300; sth++){
            System.out.println("Będę robił prace domową");
        }
    }
}
